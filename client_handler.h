#ifndef CHAOS_PROXY_CLIENT_HANDLER_H
#define CHAOS_PROXY_CLIENT_HANDLER_H

#include <string>

/**
 * Класс ClientHandler отвечает за обработку подключения одного клиента.
 *
 * Логика работы:
 * 1. Читаем HTTP-запрос от клиента
 * 2. Парсим запрос, извлекаем хост и query-параметры
 * 3. Соединяемся с целевым сервером (например, httpbin.org)
 * 4. Пересылаем запрос на целевой сервер
 * 5. Получаем ответ от целевого сервера
 * 6. Применяем "хаос" (задержку, ошибку, обрезку) согласно параметрам
 * 7. Отправляем ответ клиенту
 *
 * Почему отдельный класс?
 * - Инкапсуляция всей логики обработки одного клиента
 * - Удобно для запуска в отдельном потоке (std::thread)
 * - Можно легко тестировать отдельно от сервера
 */
class ClientHandler {
public:
    /**
     * Конструктор принимает файловый дескриптор подключенного клиента.
     * @param client_socket Сокет подключенного клиента.
     */
    explicit ClientHandler(int client_socket);

    /**
     * Деструктор закрывает сокет клиента.
     */
    ~ClientHandler();

    /**
     * Запускает обработку клиента.
     * Этот метод должен вызываться в отдельном потоке.
     */
    void handle();

private:
    int client_socket_;  // Сокет клиента

    /**
     * Читает данные из сокета до конца (пока сервер не закроет соединение).
     *
     * @param socket Файловый дескриптор сокета для чтения.
     * @return Строка с полученными данными, или пустая строка при ошибке.
     *
     * Как это работает:
     * 1. Читаем порциями по 4096 байт в цикле.
     * 2. Продолжаем пока recv() не вернет 0 (соединение закрыто) или ошибку.
     */
    std::string readFromSocket(int socket);

    /**
     * Отправляет данные в сокет.
     *
     * @param socket Файловый дескриптор сокета.
     * @param data Данные для отправки.
     * @return true если отправка успешна, false при ошибке.
     */
    bool writeToSocket(int socket, const std::string& data);

    /**
     * Создает соединение с удаленным сервером.
     *
     * @param host Имя хоста (например, "httpbin.org").
     * @param port Порт (обычно 80 для HTTP).
     * @return Файловый дескриптор сокета, или -1 при ошибке.
     */
    int connectToHost(const std::string& host, int port = 80);
};

#endif // CHAOS_PROXY_CLIENT_HANDLER_H
