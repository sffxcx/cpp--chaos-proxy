#ifndef CHAOS_PROXY_SERVER_H
#define CHAOS_PROXY_SERVER_H

#include <string>
#include <atomic>
#include <functional>

/**
 * Класс TcpServer отвечает за создание серверного сокета и прием подключений.
 *
 * Это простой TCP-сервер, который:
 * 1. Создает сокет (socket)
 * 2. Привязывает его к порту (bind)
 * 3. Начинает слушать (listen)
 * 4. Принимает подключения в цикле (accept)
 *
 * Почему мы используем отдельный класс?
 * - Инкапсуляция: вся логика работы с серверным сокетом в одном месте.
 * - Удобство: можно легко запустить/остановить сервер.
 * - Тестируемость: можно замокать этот класс для тестов.
 */
class TcpServer {
public:
    /**
     * Конструктор принимает порт для прослушивания.
     * @param port Порт, на котором будет работать прокси (например, 8080).
     */
    explicit TcpServer(int port);

    /**
     * Деструктор закрывает сокет при уничтожении объекта.
     */
    ~TcpServer();

    /**
     * Запускает сервер в бесконечном цикле.
     * Метод блокирующий - он не вернет управление пока сервер не будет остановлен.
     *
     * Для каждого нового клиентского подключения вызывается callback-функция,
     * которая передается как параметр. Это позволяет отделить логику приема
     * подключений от логики обработки клиентов.
     *
     * @param client_handler Функция, которая обрабатывает каждое подключение.
     *                       Принимает файловый дескриптор клиента.
     */
    void run(const std::function<void(int)>& client_handler);

    /**
     * Останавливает сервер.
     * После вызова этого метода run() завершит свой цикл.
     */
    void stop();

    /**
     * Проверяет, работает ли сервер.
     */
    bool isRunning() const;

private:
    int server_socket_;        // Файловый дескриптор серверного сокета
    int port_;                 // Порт для прослушивания
    std::atomic<bool> running_; // Флаг для остановки сервера (атомарный для потокобезопасности)

    /**
     * Вспомогательный метод для создания и настройки сокета.
     * Вызывается в конструкторе.
     */
    void createSocket();
};

#endif // CHAOS_PROXY_SERVER_H